#!/usr/bin/env node
// ── Edge Function flattener ─────────────────────────────────────────
// Supabase Edge Functions here are deployed via the Dashboard's paste
// editor (no CLI/Docker in this environment), which means every
// function must be a single file — but the source is written as
// index.ts + shared modules under _shared/ for sanity. Hand-flattening
// those by copy-paste is exactly how bugs like a duplicate
// `const RAZORPAY_KEY_ID` (or a duplicate `SupabaseClient` import,
// since both supabaseClients.ts and fulfillOrder.ts import it from
// the same package) creep in — the function then fails to even boot,
// which surfaces to the browser as "Failed to send a request to the
// Edge Function" since a crashed function can't return CORS headers.
//
// This script inlines each function's local `../_shared/*.ts` imports
// exactly once (even if referenced transitively by more than one
// shared file), dedupes external package imports across all of them,
// and writes one paste-ready .ts file per function — so there's
// nothing left to get wrong by hand.
//
// Usage:
//   node scripts/flatten-functions.mjs              # flattens all functions
//   node scripts/flatten-functions.mjs <name>        # flattens just one
//
// Output: supabase/functions_flattened/<name>.ts

import { readFileSync, writeFileSync, mkdirSync, readdirSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const FUNCTIONS_DIR = join(__dirname, '..', 'supabase', 'functions');
const OUT_DIR = join(FUNCTIONS_DIR, '..', 'functions_flattened');

const LOCAL_IMPORT_RE = /^import\s+\{([^}]+)\}\s+from\s+['"](\.\.?\/[^'"]+)['"];?\s*$/gm;
// Named imports only ('import { A, B } from "module"') — every external
// import in this codebase is this shape (no default/namespace imports),
// so this is deliberately narrow rather than a general ES module parser.
const EXTERNAL_IMPORT_RE = /^import\s+\{([^}]+)\}\s+from\s+['"]((?!\.\.?\/)[^'"]+)['"];?\s*$/gm;

function resolveLocalPath(fromFile, importPath) {
  // All local imports here are of the form '../_shared/xyz.ts' or './xyz.ts'
  const base = dirname(fromFile);
  let resolved = join(base, importPath);
  if (!resolved.endsWith('.ts')) resolved += '.ts';
  return resolved;
}

function flattenFile(entryPath) {
  // module specifier -> Set of imported symbol names. Two files each
  // importing a different subset of names from the same module (e.g.
  // supabaseClients.ts imports { createClient, SupabaseClient } while
  // fulfillOrder.ts imports just { SupabaseClient }, both from the same
  // jsr package) must merge into ONE import statement — deduping by
  // exact import-line text isn't enough, since those two lines aren't
  // textually identical but importing both as separate statements
  // redeclares `SupabaseClient` and fails to even parse.
  const externalImports = new Map();
  const inlinedBodies = [];
  const seenLocalFiles = new Set();

  function processFile(filePath, isEntry) {
    const real = filePath; // already resolved
    if (seenLocalFiles.has(real)) return; // already inlined — dedupe transitively-shared deps
    seenLocalFiles.add(real);

    let src = readFileSync(real, 'utf8');

    // Collect + strip external imports (jsr:, npm:, https:, bare specifiers)
    src = src.replace(EXTERNAL_IMPORT_RE, (line, namesRaw, moduleSpecifier) => {
      const names = namesRaw.split(',').map(s => s.trim()).filter(Boolean);
      const set = externalImports.get(moduleSpecifier) || new Set();
      names.forEach(n => set.add(n));
      externalImports.set(moduleSpecifier, set);
      return '';
    });

    // Recurse into local imports FIRST (so dependencies land above their
    // dependents in the output), then strip the import line itself.
    const localImportLines = [...src.matchAll(LOCAL_IMPORT_RE)];
    src = src.replace(LOCAL_IMPORT_RE, '');

    for (const match of localImportLines) {
      const importPath = match[2];
      const depPath = resolveLocalPath(real, importPath);
      processFile(depPath, false);
    }

    const header = isEntry
      ? `// ═══ ${filePath.split('/functions/')[1]} (entry point) ═══`
      : `// ═══ inlined: ${filePath.split('/functions/')[1]} ═══`;
    inlinedBodies.push(`${header}\n${src.trim()}\n`);
  }

  processFile(entryPath, true);

  const mergedImportLines = [...externalImports.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([moduleSpecifier, names]) =>
      `import { ${[...names].sort().join(', ')} } from '${moduleSpecifier}';`
    );

  const banner = [
    '// ─────────────────────────────────────────────────────────────',
    '// AUTO-GENERATED by scripts/flatten-functions.mjs — do not hand-edit.',
    '// Regenerate with: node scripts/flatten-functions.mjs',
    '// Paste this whole file into the Supabase Dashboard function editor.',
    '// ─────────────────────────────────────────────────────────────',
    '',
  ].join('\n');

  return banner + mergedImportLines.join('\n') + '\n\n' + inlinedBodies.join('\n');
}

function listFunctionDirs() {
  return readdirSync(FUNCTIONS_DIR).filter(name => {
    if (name.startsWith('_')) return false;
    const full = join(FUNCTIONS_DIR, name);
    return statSync(full).isDirectory();
  });
}

function main() {
  const only = process.argv[2];
  const targets = only ? [only] : listFunctionDirs();
  mkdirSync(OUT_DIR, { recursive: true });

  for (const name of targets) {
    const entry = join(FUNCTIONS_DIR, name, 'index.ts');
    try {
      const flattened = flattenFile(entry);
      const outPath = join(OUT_DIR, `${name}.ts`);
      writeFileSync(outPath, flattened, 'utf8');
      console.log(`✓ ${name} → functions_flattened/${name}.ts`);
    } catch (err) {
      console.error(`✗ ${name} failed: ${err.message}`);
      process.exitCode = 1;
    }
  }
}

main();
